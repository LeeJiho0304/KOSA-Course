package ch02.sec01;

public class VariableInitializationExample {
	public static void main(String[] args) {
		
		int age;
		age = 15;
		System.out.println(age);
		
		int value = 30;
		int value2;
		value2 = 5;
		
		int result = value + 10;
		System.out.println(result);
		System.out.println(value2 + result);
		
	}
}
